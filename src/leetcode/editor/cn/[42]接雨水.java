class Solution42 {
    public int trap(int[] height) {
        int n = height.length;
        int[] l_max = new int[n];
        int[] r_max = new int[n];
        for (int i = 1;i < n; ++i) {
            l_max[i] = Math.max(l_max[i-1], height[i-1]);
        }
        for (int i = n-2;i >= 0;--i) {
            r_max[i] = Math.max(r_max[i + 1], height[i + 1]);
        }
        int res = 0;
        for(int i = 0;i < n;++i) {
            int h = Math.min(l_max[i], r_max[i]);
            if (h > height[i]) {
                res += h - height[i];
            }
        }
        return res;
    }
}
